package com.kelompok3.posamplang.models;

public class PengeluaranProdukSummary {
    private String nama_produk;
    private double total_pengeluaran;

    public String getNama_produk() { return nama_produk; }
    public void setNama_produk(String nama_produk) { this.nama_produk = nama_produk; }
    public double getTotal_pengeluaran() { return total_pengeluaran; }
    public void setTotal_pengeluaran(double total_pengeluaran) { this.total_pengeluaran = total_pengeluaran; }
}
